    //mostrar todas las categorias
    private void CargarTablaCategoria() {
        Connection con = (Connection) Conecxion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "select idCategoria, descripcion,estado from tb_categoria";
        Statement st;
        try {
            st = (Statement) con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            InterGestionarCategorias.jTable_categorias = new JTable(model);
            InterGestionarCategorias.jScrollPane1.setViewportView(InterGestionarCategorias.jTable_categorias);

            model.addColumn("idCategoria");
            model.addColumn("descripcion");
            model.addColumn("estado");
            while (rs.next()) {
                Object fila[] = new Object[3];
                for (int i = 0; i < 3; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            con.close();

        } catch (SQLException e) {
            System.out.println("erros al llenar tabla" + e);
        }
        jTable_categorias.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_categorias.rowAtPoint(e.getPoint());
                int columna_point = 0;
                if (fila_point > -1) {
                    idCategoria = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosCategoriaSelecinada(idCategoria);
                }
            }
        });
        {

        }

    }

    private void EnviarDatosCategoriaSelecinada(int idCategoria) {
        try {
            try (Connection con = (Connection) Conecxion.conectar()) {
                PreparedStatement pst = (PreparedStatement) con.prepareStatement("select* from tb_categoria=" + idCategoria + "");
                ResultSet rs = pst.executeQuery();
                
                if (rs.next()) {
                    txt_descripcion.setText(rs.getString("descripcion"));
                }
            }
        } catch (SQLException e) {
            System.out.println("error al cargaar categori" + e);
        }
    }

