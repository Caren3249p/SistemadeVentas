    private void jButton2_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_eliminarActionPerformed
        if (!txt_descripcion.getText().isEmpty()) {
            Categoria categoria = new Categoria();
            Ctrl_Categoria controlCategoria = new Ctrl_Categoria();

            categoria.setDescripcion(txt_descripcion.getText().trim());
            if (controlCategoria.eliminar(idCategoria)) {
                JOptionPane.showConfirmDialog(null, "Categoria actializad");
                txt_descripcion.setText("");
                this.CargarTablaCategoria();
            } else {
                JOptionPane.showConfirmDialog(null, "error al eliminar categoria");
            }
        } else {
            JOptionPane.showConfirmDialog(null, "seleccione una categoria");
        }

    }//GEN-LAST:event_jButton2_eliminarActionPerformed

